                collected, used, and shared by them. Read the third parties’ privacy policies to see the choices they offer
                you.</p>
            <p> You can control which third-party services you connect to your account and what personal data they can
                collect about you. For example, to manage the permissions, go to the Settings in your Boxeon account.</p>
